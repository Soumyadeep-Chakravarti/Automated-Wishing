# Define the birthday message template
def generate_birthday_message(contact_name):
    return f"Happy Birthday, {contact_name}! 🎉 Wishing you a wonderful year ahead filled with joy, success, and happiness! 🥳"

# Define the general event message template
def generate_event_message(event_name, contact_name):
    return f"Happy {event_name}, {contact_name}! 🎉 May this special day bring you peace, happiness, and prosperity! 🌟"
